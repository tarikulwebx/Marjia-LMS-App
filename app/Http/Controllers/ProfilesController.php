    public function reset_course_read($course_id) {
        $course = Course::findOrFail($course_id);

        $result = false;
        foreach ($course->lessons as $lesson) {
            foreach ($lesson->reads as $read) {
                if ($read['user_id'] == Auth::user()->id) {
                    $read->delete();
                    $result = true;
                }
            }
        }

        if ($result ==true) {
            return 1;
        } else {
            return 0;
        }
    }

